package com.weshare.pojo;

import java.util.List;

public class Data {
	
	private String name;
	
	private List data;
	
	private float y;
	

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List getData() {
		return data;
	}

	public void setData(List data) {
		this.data = data;
	}
	
	

}
